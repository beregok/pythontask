## За даним натуральним числом N виведіть таке найменше ціле число k, що 2ᵏ≥N.
##
## Операцією зведення в ступінь користуватися не можна!
##
## Формат введення
##
## Вводиться натуральне число.
##
## Формат виведення
##
## Виведіть відповідь до задачі.
n = int(input())
i = 1
z = 0
while i < n:
    i = i * 2
    z = z + 1
print(z)
