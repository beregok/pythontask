# Колір клітинок шахової дошки
# Задані дві клітини шахової дошки. Якщо вони пофарбовані в один колір, то виведіть слово YES, а якщо в різні кольори - то NO.

## Формат введення

# Вводяться 4 числа - координати клітин.

## Формат виведення

# Виведіть відповідь до задачі.

x1 = int(input())
y1 = int(input())
x2 = int(input())
y2 = int(input())
if (x1 + y1) % 2 == 0 and (x2 + y2) % 2 == 0:
    print("YES")
elif (x1 + y1) % 2 != 0 and (x2 + y2) % 2 != 0:
    print("YES")
else:
    print("NO")
