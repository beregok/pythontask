## За даним натуральним n обчисліть суму 1² + 2² + 3² + ... + n².
##
## Формат введення
##
## Вводиться натуральне число.
##
## Формат виведення
##
## Виведіть відповідь до задачі.

# 1
n = int(input())
suma = 0
while n != 0:
    suma += n**2
    n -= 1
print(suma)

# 2

n = int(input())
i = 1
suma = 0
while i <= n:
    suma += i**2
    i += 1
print(suma)
