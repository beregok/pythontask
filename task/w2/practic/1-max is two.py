# Напишіть програму, яка зчитує два цілих числа A і B і виводить найбільше значення з них. Числа - цілі від 1 до 1000.

## Формат введення

# Вводяться два числа.

## Формат виведення

# Виведіть відповідь до задачі.


a = int(input())
b = int(input())
if a < b:
    print(b)
if a > b:
    print(a)
if a == b:
    print(a)
